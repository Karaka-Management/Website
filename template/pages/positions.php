<h1>Developer</h1>
<h2>Frontend</h2>
<p>As a frontend developer you would be taking care of HTML, CSS, JavaScript. You could either work on the core
    functionality or implement the frontend of modules. The project is not using any major JavaScript frameworks which
    is why talented JavaScript developers that know vanilla JS and have great ideas to implement universal solutions for
    many UI problems are highly welcome.</p>
<p>The core frontend framework tries to universally handle forms and basic UI elements in a project specific way. There
    is still a lot of basic functionallity that needs to be implemented (e.g. request pooling, simple form intaraction,
    nicer form UI elements) but besides that there are also other interesting side projects such as a charting library
    implementation based on D3, a markdown parser for project specific custom elements, math evaluator, table UI, and
    much more.</p>
<h2>Backend</h2>
<p>The backend development is not based on one of the existing frameworks and only uses a rather limited set of
    libraries. As a backend developer you would be responsible for implementing and maintaining core functionalities.
    This would also include benchmarking and improving the performance of bottlenecks. Besides these there are still a
    view open core elements that need to be fully implemented such as a markdown parser that also supports project
    specific elements, math library for financial calculations, another very big bullet point would be the
    implementation of multiple caching mechanisms (Memcached &amp; Radis) and last but not least the query builder and
    data mapper which both need to be fully implemented.</p>
<p>Another possibility would be to take over the backend development of a module which includes implementing all its
    functionality for the frontend and api including the database schema. Here you can make full use of your creativity
    and potentially implement features that you would like to have as a customer.</p>
<p>One more future playground could be the implementation of a socket in order to handle concurrency, websockets,
    multi-threaded tasks etc.</p>
<h1>Administrator</h1>
<p>A sophisticated build system is very important to support the development process. Setting up a build system
    including jenkins, unit testing, code style checking, preprocessing etc. is highly required and could be your
    responsibility. Not only would you be responsible for the build process but also for the deployment to the testing
    environment.</p>